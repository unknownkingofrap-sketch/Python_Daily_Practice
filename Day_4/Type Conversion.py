# Type Conversion

x = 1      # int

y = 1.8    # float

z = 1j     # complex number (Note: You cannot convert complex numbers into another number type.)

# convert int to float

a = float(x)

# convert float to int

b = int(y)

# convert int to complex

c = complex(x)

# printing new types

print(a)
print(b)
print(c)

# printing types

print(type(a))
print(type(b))
print(type(c))

